public class DataTypes {
    public static void main(String[] args) {
        byte b = 9;
        System.out.println(b);

        char ch = 'a';
        System.out.println(ch);

        boolean var = true;
        System.out.println(var);

        double price = 9.9;
        System.out.println(price);

        int num = 250;
        System.out.println(num);
        // long
        // float

        short n = 123;
        System.out.println(n);

    }
}
